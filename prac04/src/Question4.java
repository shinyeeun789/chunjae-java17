public class Question4 {

    public static void main(String[] args) {
        /* 다음 for문과 동일한 기능을 수행하는 while문 작성
            for (int i=10; i>0; i-=2) {
                System.out.println(i);
            }
        */

        int i = 10;
        while(i > 0) {
            System.out.println(i);
            i -= 2;
        }

    }

}
